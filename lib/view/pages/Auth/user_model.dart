class User {
  String name;
  String email;
  String password;
  String profilePhoto;
  String phoneNumber;
  String city;
  String government;
  String country;

  User({
    required this.name,
    required this.email,
    required this.password,
    required this.profilePhoto,
    required this.phoneNumber,
    required this.city,
    required this.government,
    required this.country,
  });
}
